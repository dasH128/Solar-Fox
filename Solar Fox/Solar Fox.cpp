void PintarMapa(int Arr[30][70]){

	
	for (int i = 0; i<30; i++)
	{
		for (int j = 0; j<70; j++)
		{
			if (Arr[i][j] == 0){
				
				SetColor(2);
				cout << " ";      // ---> ( )
			}
			else if (Arr[i][j] == 1){
				SetColor(3);
				
				cout << char(176); //---> (░)
			}
			else if (Arr[i][j] == 6){
				SetColor(4);
		
				cout << char(6);   // --> (♠)
			}
		}
		cout << endl;
	}
	cout << endl;
}
